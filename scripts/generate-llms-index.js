#!/usr/bin/env node
/**
 * Postbuild script: overwrite dist/llms.txt with a complete llmstxt.org-format index.
 *
 * This runs after `astro build` so it overwrites the starlight-llms-txt plugin output.
 * Purpose: add individual page URLs so AI crawlers (e.g. afdocs) can verify full coverage.
 *
 * Usage: node scripts/generate-llms-index.js
 */

import { readFile, writeFile, readdir, stat } from 'node:fs/promises'
import { join, relative, extname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = fileURLToPath(new URL('.', import.meta.url))
const ROOT = join(__dirname, '..')
const DOCS_DIR = join(ROOT, 'src/content/docs')
const OUT_FILE = join(ROOT, 'dist/llms.txt')
const BASE_URL = 'https://docs.scalekit.com'

// Custom set slugs as generated by starlight-llms-txt (& becomes --)
const CUSTOM_SETS = [
  {
    label: 'SaaSKit Complete',
    slug: 'saaskit-complete',
    desc: 'users, orgs, sessions, RBAC, login flows, SSO, SCIM provisioning',
  },
  {
    label: 'Agent Authentication',
    slug: 'agentkit',
    desc: 'AI agents, OAuth token vault, tool calling, connectors',
  },
  {
    label: 'MCP Authentication',
    slug: 'mcp-authentication',
    desc: 'remote MCP servers, OAuth 2.1, Dynamic Client Registration',
  },
  {
    label: 'Enterprise SSO & SCIM',
    slug: 'enterprise-sso--scim',
    desc: 'SAML/OIDC, directory sync, provisioning',
  },
  { label: 'Quickstart Collection', slug: 'quickstart-collection', desc: 'getting started guides' },
  {
    label: 'API & SDK Reference',
    slug: 'api--sdk-reference',
    desc: 'endpoints, SDK methods, webhooks',
  },
  {
    label: 'Integration Guides',
    slug: 'integration-guides',
    desc: 'provider-specific setup guides',
  },
  {
    label: 'Machine-to-Machine Auth',
    slug: 'machine-to-machine-auth',
    desc: 'service-to-service, client credentials, API keys',
  },
]

/**
 * Parse YAML frontmatter from a markdown/mdx file.
 * Returns { title, description, draft } — all other fields ignored.
 */
function parseFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/)
  if (!match) return {}
  const fm = match[1]
  const get = (key) => {
    const m = fm.match(new RegExp(`^${key}:\\s*['"]?(.+?)['"]?\\s*$`, 'm'))
    return m ? m[1].trim() : undefined
  }
  return {
    title: get('title'),
    description: get('description'),
    draft: get('draft') === 'true',
  }
}

/**
 * Walk a directory recursively and yield all .md/.mdx file paths.
 */
async function* walk(dir) {
  for (const entry of await readdir(dir)) {
    const full = join(dir, entry)
    if ((await stat(full)).isDirectory()) {
      yield* walk(full)
    } else {
      const ext = extname(entry)
      if (ext === '.md' || ext === '.mdx') yield full
    }
  }
}

/**
 * Convert a docs file path to its public URL path.
 * e.g. src/content/docs/authenticate/fsa/index.mdx → /authenticate/fsa/
 */
function fileToUrlPath(filePath) {
  let rel = relative(DOCS_DIR, filePath)
  // Remove extension
  rel = rel.replace(/\.(mdx?|md)$/, '')
  // Normalize separators
  rel = rel.replace(/\\/g, '/')
  // Remove trailing /index
  if (rel === 'index' || rel.endsWith('/index')) {
    rel = rel.slice(0, -'index'.length).replace(/\/$/, '')
  }
  return rel ? `/${rel}/` : '/'
}

// --- Collect all docs ---
const reads = []
for await (const file of walk(DOCS_DIR)) {
  reads.push(readFile(file, 'utf8').then((content) => ({ file, content })))
}
const allFiles = await Promise.all(reads)

const pages = []
for (const { file, content } of allFiles) {
  const { title, description, draft } = parseFrontmatter(content)
  if (draft) continue
  const urlPath = fileToUrlPath(file)
  pages.push({ file, urlPath, title: title || basename(file), description: description || '' })
}

// Sort: root first, then alphabetically by path
pages.sort((a, b) => {
  if (a.urlPath === '/') return -1
  if (b.urlPath === '/') return 1
  return a.urlPath.localeCompare(b.urlPath)
})

// --- Build the llms.txt content ---
const lines = [
  '# Scalekit',
  '',
  '> Scalekit is a developer platform for enterprise authentication, providing Full Stack Auth (FSA), Single Sign-On (SSO), SCIM provisioning, Agent Authentication, MCP Authentication, and API authentication solutions for B2B and AI applications.',
  '',
  '## How to use',
  '',
  'When answering questions about Scalekit, load the most relevant documentation set below.',
  'Use `llms-full.txt` only when exhaustive context is needed.',
  '',
  '## Documentation Sets',
  '',
  `- [Abridged documentation](${BASE_URL}/llms-small.txt): compact version with non-essential content removed`,
  `- [Complete documentation](${BASE_URL}/llms-full.txt): full documentation for Scalekit`,
  ...CUSTOM_SETS.map(
    ({ label, slug, desc }) => `- [${label}](${BASE_URL}/_llms-txt/${slug}.txt): ${desc}`,
  ),
  '',
  '## Optional',
  '',
  `- [API reference markdown](${BASE_URL}/apis.md): LLM-friendly Markdown from the Scalekit OpenAPI specification`,
  `- [OpenAPI Specification](${BASE_URL}/api/scalekit.scalar.json): OpenAPI Specification for Scalekit REST API`,
  '',
  '## All documentation pages',
  '',
]

for (const { urlPath, title, description } of pages) {
  const mdUrl = `${BASE_URL}${urlPath.replace(/\/$/, '')}.md`
  lines.push(description ? `- [${title}](${mdUrl}): ${description}` : `- [${title}](${mdUrl})`)
}

const output = lines.join('\n') + '\n'
await writeFile(OUT_FILE, output, 'utf8')

console.log(
  `[generate-llms-index] Wrote ${pages.length} pages to dist/llms.txt (${output.length} bytes)`,
)
